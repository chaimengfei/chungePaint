    <view class="brand-card">
      <view class="brand-logo-section">
        <view class="logo-container">
          <view class="text-logo">
            <text class="logo-text">贸彩</text>
            <text class="logo-tm">®</text>
          </view>
        </view>
        <view class="brand-text">
          <text class="desc-text">汽车漆、工业漆、雕塑&广告牌漆、各种辅料供应</text>
          <view class="contact-info">
            <text class="phone-icon">📞</text>
            <text class="phone-icon"> </text>
            <text class="contact-text">李增春-13161621688</text>
          </view>
        </view>
      </view>
    </view>
    
    <!-- 搜索框 -->
    <view class="search-container">
      <view class="search-box">
        <text class="search-icon">🔍</text>
        <input 
          class="search-input" 
          type="text" 
          placeholder="搜索商品名称..."
          v-model="searchKeyword"
          @input="onSearchInput"
        />
        <view v-if="searchKeyword" class="clear-btn" @click="clearSearch">
          <text class="clear-icon">✕</text>
        </view>
      </view>
    </view>
    
    <!-- 商品区域 -->
    <view class="product-container">
      <!-- 左侧分类栏 -->
      <scroll-view class="category-list" scroll-y>
        <view 
          v-for="item in categories" 
          :key="item.id"
          class="category-item"
          :class="{ active: activeCategory === item.id }"
          @click="changeCategory(item.id)"
        >
          {{ item.name }}
        </view>
      </scroll-view>
      
      <!-- 右侧商品列表 -->
      <scroll-view class="product-list" scroll-y>
      <view v-if="currentProducts.length > 0">
        <view 
          v-for="product in currentProducts" 
          :key="product.id"
          class="product-item"
        >
          <image class="product-image" :src="product.image" mode="aspectFill" />
          <view class="product-info">
            <text class="product-name">{{ product.name }}</text>
            <text class="product-price">¥{{ product.seller_price }}</text>
            <text class="product-unit">{{ product.unit }}</text>
            <view class="action-buttons">
              <button class="add-btn" @click="addToCart(product.id)">加入购物车</button>
              <button class="buy-btn" @click="buyNow(product)">立即购买</button>
            </view>
          </view>
        </view>
      </view>
      <view v-else class="empty">
        暂无商品
      </view>
      </scroll-view>
    </view>
  </view>
</template>

<script>
import { getProductList } from '@/api/product.js'
import { addToCart, getCartList } from '@/api/cart.js'
import { checkoutOrder } from '@/api/order.js'
import { goLogin } from '@/api/user.js'

export default {
  data() {
    return {
      categories: [],         // 分类列表
      products: {},           // 所有商品
      activeCategory: null,   // 当前选中的分类ID
      currentProducts: [],    // 当前显示的商品列表
      isLogin: false,         // 登录状态
      userInfo: {},           // 用户信息
      currentTime: '09:16',   // 当前时间
      searchKeyword: '',      // 搜索关键词
      isSearching: false,     // 是否正在搜索
      originalCategories: null, // 原始分类数据（用于搜索后恢复）
      originalProducts: null    // 原始商品数据（用于搜索后恢复）
    }
  },
  onLoad() {
    // 检查登录状态并自动登录
    this.checkLoginStatus()
    // 加载商品数据
    this.fetchData()
  },
  onShow() {
    // 每次显示首页时更新购物车徽标
    this.updateCartBadge()
  },
  onShareAppMessage() {
    // 分享给微信好友
    return {
      title: '贸彩漆业 - 汽车漆、工业漆、雕塑&广告牌漆供应',
      desc: '联系人 李增春-13161621688',
      path: '/pages/index/index',
      imageUrl: '/static/images/share-logo.png' // 可以设置分享图片
    }
  },
  onShareTimeline() {
    // 分享到朋友圈
    return {
      title: '贸彩漆业 - 汽车漆、工业漆、雕塑&广告牌漆供应',
      query: '',
      imageUrl: '/static/images/share-logo.png' // 可以设置分享图片
    }
  },
  methods: {
    // 检查登录状态
    checkLoginStatus() {
      const token = uni.getStorageSync('token')
      const userInfo = uni.getStorageSync('userInfo')
      
      if (token && userInfo) {
        // 已登录
        this.isLogin = true
        this.userInfo = userInfo
        console.log('用户已登录:', userInfo)
      } else {
        // 未登录，自动登录
        this.autoLogin()
      }
    },
    
    // 自动登录
    async autoLogin() {
      try {
        console.log('开始自动登录流程...')
        
        // 1. 获取微信登录code
        const loginRes = await new Promise((resolve, reject) => {
          uni.login({
            success: resolve,
            fail: reject
          })
        })

        const code = loginRes.code
        if (!code) {
          console.error('无法获取微信登录code')
          return
        }

        // 2. 获取用户信息（昵称和头像）
        const userInfoRes = await new Promise((resolve, reject) => {
          uni.getUserProfile({
            desc: '用于完善用户资料',
            success: resolve,
            fail: (err) => {
              console.warn('用户拒绝授权获取用户信息:', err)
              // 如果用户拒绝授权，使用默认信息
              resolve({
                userInfo: {
                  nickName: '微信用户',
                  avatarUrl: '/static/images/default-avatar.png'
                }
              })
            }
          })
        })

        const { nickName: nickname, avatarUrl: avatar } = userInfoRes.userInfo
        
        // 确保avatar是完整的URL格式
        console.log('获取到的微信头像URL:', avatar)
        
        // 验证和处理头像URL
        let finalAvatar = avatar
        if (!avatar || !avatar.startsWith('http')) {
          // 如果头像URL无效，使用默认头像
          finalAvatar = '/static/images/default-avatar.png'
          console.warn('微信头像URL无效，使用默认头像:', finalAvatar)
        }

        // 3. 检查是否首次登录（需要传递地理位置）
        const hasStoredUserInfo = uni.getStorageSync('hasStoredUserInfo')
        let loginData = {
          code: code,
          nickname: nickname,
          avatar: finalAvatar
        }

        // 只在首次登录时获取和传递地理位置
        if (!hasStoredUserInfo) {
          console.log('首次登录，获取地理位置信息...')
          const locationRes = await new Promise((resolve, reject) => {
            uni.getLocation({
              type: 'gcj02',
              success: resolve,
              fail: (err) => {
                console.warn('获取地理位置失败:', err)
                // 如果获取地理位置失败，使用默认值
                resolve({
                  latitude: 39.9042,  // 北京默认纬度
                  longitude: 116.4074  // 北京默认经度
                })
              }
            })
          })

          const { latitude, longitude } = locationRes
          loginData.latitude = latitude
          loginData.longitude = longitude
          console.log('首次登录，传递地理位置:', { latitude, longitude })
        } else {
          console.log('非首次登录，跳过地理位置获取')
        }

        console.log('调用登录接口，数据:', loginData)
        const loginApiRes = await goLogin(loginData)
        
        if (loginApiRes.data.code === 0) {
          const { token, user_info } = loginApiRes.data.data

          // 5. 存储登录信息
          uni.setStorageSync('token', token)
          uni.setStorageSync('userInfo', user_info)
          // 标记用户信息已存储（用于判断是否首次登录）
          uni.setStorageSync('hasStoredUserInfo', true)

          // 6. 设置全局请求头
          uni.$u.http.setConfig((config) => {
            config.header.Authorization = `Bearer ${token}`
            return config
          })

          // 7. 更新页面状态
          this.isLogin = true
          this.userInfo = user_info
          
          console.log('自动登录成功:', user_info)
          
          // 8. 显示欢迎提示
          const wasFirstLogin = !uni.getStorageSync('hasStoredUserInfo')
          const welcomeMsg = wasFirstLogin ? `欢迎注册，${user_info.nickname || '用户'}` : `欢迎回来，${user_info.nickname || '用户'}`
          uni.showToast({
            title: welcomeMsg,
            icon: 'success',
            duration: 2000
          })
        } else {
          console.error('登录接口返回错误:', loginApiRes.data.message)
        }
      } catch (err) {
        console.error('自动登录失败:', err)
        // 登录失败不影响商品展示，静默处理
      }
    },
    
    async fetchData() {
      try {
        const res = await getProductList()
        this.categories = res.categories
        this.products = res.products
        
        // 保存原始数据（用于搜索后恢复）
        this.originalCategories = res.categories
        this.originalProducts = res.products
        
        // 默认选中第一个分类
        if (this.categories.length > 0) {
          this.activeCategory = this.categories[0].id
          this.currentProducts = this.products[this.activeCategory] || []
        }
      } catch (err) {
        uni.showToast({
          title: '数据加载失败',
          icon: 'none'
        })
      }
    },
    
    changeCategory(categoryId) {
      this.activeCategory = categoryId
      this.currentProducts = this.products[categoryId] || []
    },
    
    // 添加商品到购物车
    async addToCart(productId) {
      try {
        const res = await addToCart({ product_id: productId })
        if (res.data.code === 0) {
          uni.showToast({
            title: '已加入购物车',
            icon: 'success'
          })
          
          // 更新底部购物车徽标
          this.updateCartBadge()
        } else {
          uni.showToast({
            title: res.data.message || '添加购物车失败',
            icon: 'none'
          })
        }
      } catch (err) {
        uni.showToast({
          title: '添加购物车失败',
          icon: 'none'
        })
      }
    },
    
    // 立即购买
    async buyNow(product) {
      try {
        uni.showLoading({ title: '处理中...' })
        
        // 调用checkout接口
        const res = await checkoutOrder({
          cart_ids: null,
          product_id: product.id,
          quantity: 1,
          address_id: null // 这里可以传入默认地址ID，或者让用户在结算页面选择
        })
        
        uni.hideLoading()
        
        if (res.data.code === 0) {
          // 跳转到结算页面
          uni.navigateTo({
            url: `/pages/order/checkout?product_id=${product.id}&quantity=1`
          })
        } else {
          uni.showToast({
            title: res.data.message || '立即购买失败',
            icon: 'none'
          })
        }
      } catch (err) {
        uni.hideLoading()
        console.error('立即购买失败:', err)
        uni.showToast({
          title: '立即购买失败',
          icon: 'none'
        })
      }
    },
    
    // 拨打电话
    makeCall() {
      uni.makePhoneCall({
        phoneNumber: '13161621688',
        success: function () {
          console.log('拨打电话成功')
        },
        fail: function (err) {
          console.log('拨打电话失败:', err)
        }
      })
    },
    
    // 跳转到个人中心
    goToProfile() {
      uni.switchTab({
        url: '/pages/user/index'
      })
    },
    
    // 分享给朋友
    shareToFriend() {
      uni.share({
        provider: 'weixin',
        scene: 'WXSceneSession',
        type: 5,
        title: '贸彩漆业 - 专业涂料供应商',
        summary: '汽车漆、工业漆、雕塑&广告牌漆供应',
        href: '/pages/index/index',
        imageUrl: '/static/images/share-logo.png',
        success: function (res) {
          console.log('分享成功')
        },
        fail: function (err) {
          console.log('分享失败:', err)
        }
      })
    },
    
    // 更新购物车徽标
    async updateCartBadge() {
      try {
        const res = await getCartList()
        if (res.data.code === 0) {
          const cartItems = res.data.data || []
          const uniqueItemCount = cartItems.length
          
          if (uniqueItemCount > 0) {
            uni.setTabBarBadge({
              index: 1,
              text: uniqueItemCount.toString()
            })
          } else {
            uni.removeTabBarBadge({
              index: 1
            })
          }
        }
      } catch (error) {
        console.error('更新购物车徽标失败:', error)
      }
    },
    
    // 搜索输入处理
    onSearchInput(e) {
      this.searchKeyword = e.detail.value
      this.performSearch()
    },
    
    // 执行搜索
    async performSearch() {
      if (!this.searchKeyword.trim()) {
        // 如果搜索关键词为空，显示当前分类的商品
        this.showCurrentCategoryProducts()
        this.isSearching = false
        return
      }
      
      this.isSearching = true
      
      try {
        // 调用后端API进行搜索
        const res = await getProductList(this.searchKeyword)
        
        console.log('搜索关键词:', this.searchKeyword)
        console.log('搜索API返回数据:', res)
        
        // 检查API返回的数据结构
        if (res && typeof res === 'object') {
          // 保存原始的分类和商品数据（用于清空搜索时恢复）
          if (!this.originalCategories) {
            this.originalCategories = this.categories
            this.originalProducts = this.products
          }
          
          // 后端返回的数据结构: {categories: [], products: {}}
          this.categories = res.categories || []
          this.products = res.products || {}
          
          // 计算所有商品数量
          const allProducts = []
          Object.values(this.products).forEach(categoryProducts => {
            if (Array.isArray(categoryProducts)) {
              allProducts.push(...categoryProducts)
            }
          })
          this.currentProducts = allProducts
          
          // 清空当前选中的分类，因为显示的是搜索结果
          this.activeCategory = null
          
          // 根据搜索结果给出不同的提示
          if (allProducts.length === 0) {
            console.log('搜索完成，未找到匹配的商品')
            // 不显示错误提示，只是显示空结果
          } else {
            console.log('搜索成功，找到商品数量:', allProducts.length)
          }
        } else {
          console.log('搜索失败，返回数据格式错误:', res)
          uni.showToast({
            title: '搜索失败，请重试',
            icon: 'none'
          })
        }
      } catch (error) {
        console.error('搜索失败:', error)
        uni.showToast({
          title: '搜索失败，请重试',
          icon: 'none'
        })
      }
    },
    
    // 清空搜索
    async clearSearch() {
      this.searchKeyword = ''
      this.isSearching = false
      
      // 恢复原始数据
      if (this.originalCategories && this.originalProducts) {
        this.categories = this.originalCategories
        this.products = this.originalProducts
        
        // 默认选中第一个分类
        if (this.categories.length > 0) {
          this.activeCategory = this.categories[0].id
          this.currentProducts = this.products[this.activeCategory] || []
        }
      } else {
        // 如果没有原始数据，重新加载
        try {
          const res = await getProductList()
          this.categories = res.categories || []
          this.products = res.products || {}
          
          // 默认选中第一个分类
          if (this.categories.length > 0) {
            this.activeCategory = this.categories[0].id
            this.currentProducts = this.products[this.activeCategory] || []
          }
        } catch (error) {
          console.error('重新加载数据失败:', error)
          uni.showToast({
            title: '重新加载失败',
            icon: 'none'
          })
        }
      }
    },
    
    // 显示当前分类的商品
    showCurrentCategoryProducts() {
      if (this.activeCategory && this.products[this.activeCategory]) {
        this.currentProducts = this.products[this.activeCategory]
      } else {
        this.currentProducts = []
      }
    }
  }
}
</script>

